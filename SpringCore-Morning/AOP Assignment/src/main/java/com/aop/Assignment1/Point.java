package com.aop.Assignment1;

public class Point {
    private int x, y;

    public Point(int x, int y){
        this.x = x;
        this.y = y;
    }

    public void setX_axis(int x) {
        this.x = x;
    }
    public int getX_axis() {
        return x;
    }

    public void setY_axis(int y) {
        this.y = y;
    }
    public int getY_axis() {
        return y;
    }

}